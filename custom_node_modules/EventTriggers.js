//Gets last conditions
module.exports.getSensorReadings = function getSensorReadings() {
  return new Promise(resolve => {
    var con = mysql.createConnection(config.database);
    con.connect((err) => {
      if(err){
        reject(err);
      } else {
        con.query('CALL getSensorLastReadings()', (error, results, fields) => {
          //Error on problem.
          if(error) {
            console.error("EventTriggers.js: getSensorReadings() failed, database error.");
            con.destroy();
            reject(error);
          } else {
            //DO STUFF WITH DATA
            con.destroy();
            resolve(results[0])
          }
        })
      }
    });
  })
}

