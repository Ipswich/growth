//Gets DS18B20 addresses from the board, assigning them to array for adding to the database. 
module.exports.DS18B20AddressFilter = function DS18B20AddressFilter(DS18B20_data, state, addr) {
  //If this address exists in database, do not include in list
  let include = true;
  for (i = 0; i < state.sensorState.getSensorState().length; i++) {
    if (state.sensorState.data[i].sensorModel == "DS18B20" && state.sensorState.data[i].sensorAddress == addr) {              
      include = false;
    }
  }
  if (include){
    DS18B20_data.array.push(addr)
  }
  DS18B20_data.count--
}


//Gets pin and count for DS18B20 sensors
module.exports.DS18B20Initializer = function DS18B20Initializer(state){
  // Get DS18B20 count and pin
  let count = 0;
  let pin = -1
  for (let i = 0; i < state.sensorState.getSensorState().length; i++) {      
    if (state.sensorState.data[i].sensorModel == "DS18B20") {
      count++
      pin = state.sensorState.data[i].sensorPin
    }
  }
  return { count, pin }
}

