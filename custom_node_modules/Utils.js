


module.exports.formatTimeString = function formatTimeString(input){
  var string = "";
  if (input == undefined || input == '' || input == null){
    return input;
  }
  input = input.substring(1, input.length-1);
  var destructed = input.split(" ");
  var time = destructed[0].split(":");
  if(destructed[1] == 'AM') {
    if(time[0] == '12'){
      time[0] = '00'
    }
  } else {
    if (time[0] != '12'){
      time[0] = parseInt(time[0]) + 12
    }
  }
  string = time[0] + ":" + time[1] + ":" + '00';
  return string
}

module.exports.formatDateString = function formatDateString(input){
  if (input == 'NULL'){
    return input;
  }
  input = input.substring(1, input.length-1);
  var string = "";
  var destructed = input.split("/");
  string = "'" + destructed[2] + "-" + destructed[0] + "-" + destructed[1] + "'"
  return string;
}

//Format time string for readability.
module.exports.formatTimeStringH = function formatTimeStringH(input){
  if (input == null){
    return "";
  }
  var destructed = input.split(":");
  if(destructed[0] < 12){
    if (destructed[0] == 0){
      return ('12' + ':' + destructed[1] + ' AM');
    } else {
      return (destructed[0] + ':' + destructed[1] + ' AM');
    }
  } else {
    if (destructed[0] == 12){
      return (destructed[0] + ':' + destructed[1] + ' PM');
    } else {
      return ((destructed[0] - 12) + ':' + destructed[1] + ' PM');
    }
  }
}
