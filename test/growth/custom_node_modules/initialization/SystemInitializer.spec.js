const chai = require('chai').assert
const OutputState = require('../../../../custom_node_modules/state_modules/OutputState.js')
const SensorState = require('../../../../custom_node_modules/state_modules/SensorState.js')
const SensorTypes = require('../../../../custom_node_modules/initialization/SensorTypes.js')
const SystemInitializer = require('../../../../custom_node_modules/initialization/SystemInitializer.js')

describe('SystemInitalizer.js Tests', () => {
  describe("createBoardObject()", () => {
    it('should return a board object', () => {
      let board = SystemInitializer.createBoardObject({board:'test'})
      chai.equal('Mock', board.io.name)
    })
  })  

  describe("createRelayControlObject()", () => {
    let board;
    before(() => {
      board = SystemInitializer.createBoardObject({board:'test'})
    })
    it('should return a relay', () => {
      config = {relay_toggle_prevention:true}
      let result = SystemInitializer.createRelayControlObject(config, board)
      chai.equal(result.io.name, 'Mock')
    })
    it('should not return a relay', () => {
      config = {relay_toggle_prevention: false}
      chai.isNull(SystemInitializer.createRelayControlObject(config, board))
    })
  })

  describe("initializeSensors()", () => {
    let DS18B20_data = {count: 0, pin: -1, array: []}
    let state = {}
    before(async () => {
      state.sensorState = await new SensorState();
    })
    it('should add sensor objects to state', async () => {
      let DS18B20_result = SensorTypes.DS18B20Initializer(state)
      DS18B20_data.count = DS18B20_result.count
      DS18B20_data.pin = DS18B20_result.pin
      chai.isNull(state.sensorState.data[0].sensorObject)
      SystemInitializer.initializeSensors(state, DS18B20_data)
      chai.equal('Mock', state.sensorState.data[0].sensorObject.io.name)
      chai.equal('Mock', state.sensorState.data[1].sensorObject.io.name)
      chai.equal('Mock', state.sensorState.data[2].sensorObject.io.name)
      chai.equal('Mock', state.sensorState.data[3].sensorObject.io.name)
      chai.equal('Mock', state.sensorState.data[4].sensorObject.io.name)
    })
  })

  describe("initializeOutputs()", () => {
    let board;
    let state = {}
    before(async () => {
      state.outputState = await new OutputState();
      board = SystemInitializer.createBoardObject({board:'test'})
    })
    it('should add output objects and their PWM objects to state', async () => {
      chai.isNull(state.outputState.data[0].outputObject)
      SystemInitializer.initializeOutputs(state, board)
      chai.equal('Mock', state.outputState.data[0].outputObject.io.name)
      chai.isNull(state.outputState.data[0].outputPWMObject)
      chai.equal('Mock', state.outputState.data[1].outputObject.io.name)
      chai.equal('Mock', state.outputState.data[1].outputPWMObject.io.name)
      chai.equal('Mock', state.outputState.data[2].outputObject.io.name)
      chai.equal('Mock', state.outputState.data[2].outputPWMObject.io.name)
    })
  })
})