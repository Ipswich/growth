const chai = require('chai').assert
const SensorState = require('../../../../custom_node_modules/state_modules/SensorState.js')
const SensorTypes = require('../../../../custom_node_modules/initialization/SensorTypes.js')

describe("SensorTypes.js Tests", () => {
  describe("DS18B20Initializer()", () => {
    let state = {}
    before(async () => {
      state.sensorState = await new SensorState();
    })
    it('should return an object with a count and pin', () => {
      let val = {count: 2, pin: 21}
      chai.deepEqual(val, SensorTypes.DS18B20Initializer(state))
    })
  })

  describe("DS18B20AddressFilter()", () => {
    let state = {}
    before(async () => {
      state.sensorState = await new SensorState();
    })
    it('should add addresses from the board to DS18B20_data.array if they don\'t exist in the database', () => {
      let DS18B20_data = {}
      DS18B20_data.count = 1;
      DS18B20_data.array = [];
      let addr1 = '0000000000002'
      SensorTypes.DS18B20AddressFilter(DS18B20_data, state, addr1)
      chai.equal(DS18B20_data.count, 0)
      chai.equal(DS18B20_data.array.length, 1)
    })

    it('should ignore addresses if they exist in the database', () => {
      let DS18B20_data = {}
      DS18B20_data.count = 1;
      DS18B20_data.array = [];
      let addr1 = '0000000000001'
      SensorTypes.DS18B20AddressFilter(DS18B20_data, state, addr1)
      chai.equal(DS18B20_data.count, 0)
      chai.equal(DS18B20_data.array.length, 0)
    })

    it('should decrement DS18B20_data.count after each pass, regardless of address presence', () => {
      let DS18B20_data = {}
      DS18B20_data.count = 3;
      DS18B20_data.array = [];
      let addr1 = '0000000000000'
      let addr2 = '0000000000001'
      let addr3 = '0000000000002'
      SensorTypes.DS18B20AddressFilter(DS18B20_data, state, addr1)
      chai.equal(DS18B20_data.count, 2)
      SensorTypes.DS18B20AddressFilter(DS18B20_data, state, addr2)
      chai.equal(DS18B20_data.count, 1)
      SensorTypes.DS18B20AddressFilter(DS18B20_data, state, addr3)
      chai.equal(DS18B20_data.count, 0)
    })
  })

  describe("I2CFilter()", () => {
    let state = {}
    before(async () => {
      state.sensorState = await new SensorState()
    })
    it('should add BME280 address to obj', () => {
      let currentState = state.sensorState.data[0]
      let counter = 1
      let obj = {controller: state.sensorState.data[0].sensorModel}
      SensorTypes.I2CFilter(counter, obj, currentState)
      chai.deepEqual(obj, { controller: 'BME280', address: '0x76' })
    })
  })

  describe("createThermometerOpts()", () => {
    let state = {}
    before(async () => {
      state.sensorState = await new SensorState()
    })

    it('should return BME280 thermometer opts', () => {
      let DS18B20_data = {};
      let DS18B20_result = SensorTypes.DS18B20Initializer(state) 
      DS18B20_data.pin = DS18B20_result.pin    
      DS18B20_data.array = [];
      let DS18B20_Array = DS18B20_data.array.map(e => e.toString())
      let currentState = state.sensorState.data[0]
      let obj = {controller: state.sensorState.data[0].sensorModel}
      SensorTypes.createThermometerOpts(currentState, DS18B20_Array, DS18B20_data, obj)
      chai.deepEqual(obj, { controller: 'BME280' })
    })

    it('should return DS18B20 thermometer opts', () => {
      let DS18B20_data = {};
      let DS18B20_result = SensorTypes.DS18B20Initializer(state) 
      DS18B20_data.pin = DS18B20_result.pin    
      DS18B20_data.array = [];
      DS18B20_data.array.push('0000000000000')
      let DS18B20_Array = DS18B20_data.array.map(e => e.toString())
      let obj = {controller: state.sensorState.data[3].sensorModel}
      let currentState = state.sensorState.data[3]
      let result = { controller: 'DS18B20', pin: 21, address: 0 }
      chai.deepEqual(SensorTypes.createThermometerOpts(currentState, DS18B20_Array, DS18B20_data, obj), result)
      chai.isEmpty(DS18B20_Array)      
    })
  })
  
  describe("createHygrometerOpts()", () => {
    let state = {} 
    before(async () => {
      state.sensorState = await new SensorState()
    })
    it('should return BME280 hygrometer opts', () => {      
      obj = {controller: state.sensorState.data[2].sensorModel}  
      SensorTypes.createHygrometerOpts(obj)      
      chai.deepEqual(obj, {controller: "BME280"})
    })
  })
  
  describe("createBarometerOpts()", () => {
    let state = {}
    before(async () => {
      state.sensorState = await new SensorState()
    })
    it('should return BME280 barometer opts', () => {
      obj = {controller: state.sensorState.data[2].sensorModel}        
      SensorTypes.createBarometerOpts(obj)
      chai.deepEqual(obj, {controller: "BME280"})
    })
  })
})